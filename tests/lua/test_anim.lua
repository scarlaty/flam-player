test("anim full lifecycle", function()
    local obj = lv.obj.new(window)
    lv.obj.set_size(obj, 50, 50)
    lv.obj.set_pos(obj, 0, 0)

    local a = lv.anim.new()
    lv.anim.set_var(a, obj)
    lv.anim.set_values(a, 0, 100)
    lv.anim.set_time(a, 100)
    lv.anim.set_path_cb(a, lv.anim.path_linear)
    lv.anim.set_exec_cb(a, function(o, val)
        lv.obj.set_x(o, val)
    end)
    lv.anim.start(a)
    test_tick(30)
    -- Object x should have moved
    expect_true(true)
    lv.obj.del(obj)
end)

test("anim_var.del", function()
    local obj = lv.obj.new(window)
    local a = lv.anim.new()
    lv.anim.set_var(a, obj)
    lv.anim.set_values(a, 0, 100)
    lv.anim.set_time(a, 1000)
    lv.anim.set_exec_cb(a, function(o, v) end)
    lv.anim.start(a)
    -- Delete animation by var
    lv.anim_var.del(a)
    expect_true(true)
    lv.obj.del(obj)
end)
