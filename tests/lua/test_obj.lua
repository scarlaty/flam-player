test("obj.clean removes children", function()
    local parent = lv.obj.new(window)
    lv.obj.new(parent)
    lv.obj.new(parent)
    expect_eq(lv.obj.get_child_cnt(parent), 2, "before clean")
    lv.obj.clean(parent)
    expect_eq(lv.obj.get_child_cnt(parent), 0, "after clean")
    lv.obj.del(parent)
end)

test("obj.del does not crash", function()
    local obj = lv.obj.new(window)
