require("test_helpers")
print("--- test_obj ---")

test("obj.new creates object", function()
    local obj = lv.obj.new(window)
    expect_true(obj ~= nil, "obj should not be nil")
end)

test("obj.new without parent uses screen", function()
    local obj = lv.obj.new()
    expect_true(obj ~= nil)
    lv.obj.del(obj)
end)

test("obj.set_size and get_width/get_height", function()
    local obj = lv.obj.new(window)
    lv.obj.set_size(obj, 100, 50)
    test_tick(1)
    expect_eq(lv.obj.get_width(obj), 100, "width")
    expect_eq(lv.obj.get_height(obj), 50, "height")
    lv.obj.del(obj)
end)

test("obj.set_width / set_height separately", function()
    local obj = lv.obj.new(window)
    lv.obj.set_width(obj, 200)
    lv.obj.set_height(obj, 100)
