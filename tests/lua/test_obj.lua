    expect_true(true)
end)

test("obj cache returns same userdata for same object", function()
    local obj = lv.obj.new(window)
    local c1 = lv.obj.get_child(window, lv.obj.get_child_cnt(window) - 1)
    -- c1 should be the same Lua userdata as obj
    expect_eq(obj, c1, "cache identity")
    lv.obj.del(obj)
end)

